package abstraction;

public class AbstractionDocs {
	/*
	 *   //========== Abstraction =============
	 *   # implementation details hiding process
	 *   
	 *   1> Abstract class
	 *   2> Interface
	 *   
	 *   //========= Abstract class ============
	 *   # class which contains abstract method as well as non-abstract 
	 *   # we can't create object of Abstract class
	 *   # every abstract class must have at least one child class
	 *      and child class must override 
	 *      
	 *      syntax:
	 *      abstract class  class-name{
	 *      
	 *         //abstract method
	 *         
	 *         //non-abstract method
	 *      
	 *      }
	 *      
	 *      e.g. 
	 *     abstract  class  Animal{
	 *      
	 *         void eat(){
	 *         
	 *         }
	 *         
	 *         abstract void move();
	 *      }
	 *      
	 *      
	 *      //========== Interface ==================
	 *      
	 *      # same as class which contains public abstract method only
	 *      # we can't create object of interface
	 *      # also known as 100% abstraction
	 *      
	 *      syntax:
	 *       interface  interface_name{
	 *       
	 *            // public static final variables
	 *           
	 *            //public abstract method
	 *       }
	 *      
	 *      e.g.
	 *       interface A{
	 *           int MAX = 7000;
	 *           void add();
	 *       }
	 *      
	 *      
	 */

}
